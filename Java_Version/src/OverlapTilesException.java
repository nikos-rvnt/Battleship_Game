
public class OverlapTilesException extends Exception{

	public OverlapTilesException() {

    }

    public OverlapTilesException(String message) {
        super(message);
    }

    public OverlapTilesException(Throwable cause) {
        super(cause);
    }

    public OverlapTilesException(String message, Throwable cause) {
        super (message, cause);
    }

}
