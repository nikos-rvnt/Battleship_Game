
public class AdjacentTilesException extends Exception{

	public AdjacentTilesException() {

    }

    public AdjacentTilesException(String message) {
        super(message);
    }

    public AdjacentTilesException(Throwable cause) {
        super(cause);
    }

    public AdjacentTilesException(String message, Throwable cause) {
        super (message, cause);
    }

}
